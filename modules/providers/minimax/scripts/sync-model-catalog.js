#!/usr/bin/env node
import { writeFile } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';

const apiKey = process.env.MINIMAX_API_KEY;
if (!apiKey) throw new Error('Set MINIMAX_API_KEY before running the MiniMax catalog sync.');

const ids = [];
let afterId = null;
do {
  const url = new URL('https://api.minimax.io/anthropic/v1/models');
  url.searchParams.set('limit', '100');
  if (afterId) url.searchParams.set('after_id', afterId);
  const response = await fetch(url, { headers: { 'X-Api-Key': apiKey } });
  if (!response.ok) throw new Error(`MiniMax model listing failed (${response.status}): ${await response.text()}`);
  const page = await response.json();
  for (const model of page.data || []) if (model?.id && !ids.includes(model.id)) ids.push(model.id);
  afterId = page.has_more ? page.last_id : null;
} while (afterId);

if (!ids.length) throw new Error('MiniMax returned an empty Anthropic-compatible model catalog.');

const source = `// Generated by scripts/sync-model-catalog.js. Keep a checked-in snapshot so\n// the public catalog works before a user has configured credentials.\nexport const minimaxTextModelIds = Object.freeze(${JSON.stringify(ids, null, 2)});\n`;
const destination = fileURLToPath(new URL('../server/generatedTextModels.js', import.meta.url));
await writeFile(destination, source, 'utf8');
console.log(`Updated ${destination} with ${ids.length} MiniMax text models.`);
